'''
Exercício 05
Cálculo de porcentagem: calcule um aumento de 18% para um salário de 
R$ 1.250,00.
'''

# Define o valor do salário
salario = 1250.00

# Calcula o aumento de 18%
aumento = salario * 0.18

# Calcula o novo salário após o aumento
novo_salario = salario + aumento

# Exibe o novo salário
print(f"O novo salário, após um aumento de 18%, é: R$ {novo_salario:.2f}")
 